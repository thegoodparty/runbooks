"""Publish PMF experiment manifests to s3://agent-experiment-metadata-{env}/.

Reads `experiments/<id>/{manifest.json,instruction.md}` from this repo,
validates every manifest against the meta-schema, then atomically publishes:

  s3://agent-experiment-metadata-{env}/<id>/manifest.json
  s3://agent-experiment-metadata-{env}/<id>/instruction.md
  s3://agent-experiment-metadata-{env}/index.json   ← written LAST

`index.json` is the file every consumer (Lambda, broker, gp-api) reads first
to discover routing — uploading it last means readers never see a partial
publish where one experiment was updated and another wasn't.

`index.json` is GENERATED by this script and gitignored — never commit it.
The contract documented by the meta-schema + each `manifest.json` is the
source of truth; a hand-edited `experiments/index.json` would silently lose
any extra fields on the next publish (the script only emits a fixed set:
`{id, version, manifest_key, instruction_key, hash}`).

Usage:
    AWS_PROFILE=work uv run python publish_experiments.py --env=dev
    AWS_PROFILE=work uv run python publish_experiments.py --env=qa --only voter_targeting

In CI: GH Actions assumes role `agent-experiment-metadata-publish-{env}` via
OIDC (no long-lived credentials).
"""

from __future__ import annotations

import argparse
import hashlib
import json
import sys
from concurrent.futures import ThreadPoolExecutor, as_completed
from datetime import datetime, timezone
from pathlib import Path

import boto3
from botocore.exceptions import ClientError
from jsonschema import Draft7Validator

REPO_ROOT = Path(__file__).resolve().parents[2]
EXPERIMENTS_DIR = REPO_ROOT / "experiments"
META_SCHEMA_PATH = EXPERIMENTS_DIR / "_schema" / "manifest.schema.json"

VALID_ENVS = {"dev", "qa", "prod"}


def _load_meta_schema() -> dict:
    return json.loads(META_SCHEMA_PATH.read_text())


def _experiment_dirs() -> list[Path]:
    return sorted(
        p for p in EXPERIMENTS_DIR.iterdir()
        if p.is_dir() and p.name != "_schema" and not p.name.startswith(".")
    )


def _hash_pair(manifest_bytes: bytes, instruction_bytes: bytes) -> str:
    h = hashlib.sha256()
    h.update(manifest_bytes)
    h.update(instruction_bytes)
    return "sha256:" + h.hexdigest()


def _validate_all(meta_schema: dict, dirs: list[Path]) -> None:
    """Fail loudly on any meta-schema violation BEFORE any S3 write."""
    validator = Draft7Validator(meta_schema)
    failed = False
    for d in dirs:
        manifest_path = d / "manifest.json"
        instruction_path = d / "instruction.md"
        if not manifest_path.exists():
            print(f"  ✗ {d.name}: missing manifest.json", file=sys.stderr)
            failed = True
            continue
        if not instruction_path.exists():
            print(f"  ✗ {d.name}: missing instruction.md", file=sys.stderr)
            failed = True
            continue
        try:
            manifest = json.loads(manifest_path.read_text())
        except json.JSONDecodeError as e:
            print(f"  ✗ {d.name}: manifest.json invalid JSON: {e}", file=sys.stderr)
            failed = True
            continue
        errors = sorted(validator.iter_errors(manifest), key=lambda e: list(e.absolute_path))
        if errors:
            print(f"  ✗ {d.name}: {len(errors)} schema violation(s):", file=sys.stderr)
            for err in errors:
                path = ".".join(str(p) for p in err.absolute_path) or "<root>"
                print(f"      {path}: {err.message}", file=sys.stderr)
            failed = True
            continue
        if manifest.get("id") != d.name:
            print(
                f"  ✗ {d.name}: manifest id '{manifest.get('id')}' does not match dir name",
                file=sys.stderr,
            )
            failed = True
            continue
        print(f"  ✓ {d.name} v{manifest['version']}")
    if failed:
        sys.exit(1)


def _resolve_json_pointer(root: dict, pointer: str):
    """Walk a JSON Pointer path like `districtInputs/properties/state` against
    the meta-schema's $defs root. Returns the targeted node or raises ValueError."""
    node = root
    for segment in pointer.split("/"):
        if not segment:
            continue
        if not isinstance(node, dict) or segment not in node:
            raise ValueError(f"$ref segment {segment!r} not found in path {pointer!r}")
        node = node[segment]
    return node


def _inline_refs(node, defs: dict):
    """Recursively inline `{"$ref": "../_schema/manifest.schema.json#/$defs/X"}`
    (and nested forms like `#/$defs/X/properties/Y`) into the actual referenced
    object. Source manifests stay DRY; published manifests are self-contained so
    consumers don't need a $ref resolver.

    Only handles `$ref` pointing at the meta-schema's `$defs` namespace. Other
    refs pass through unchanged so a future absolute-URI ref doesn't get
    silently broken.
    """
    if isinstance(node, dict):
        ref = node.get("$ref")
        if isinstance(ref, str) and "#/$defs/" in ref:
            pointer = ref.split("#/$defs/", 1)[1]
            try:
                target = _resolve_json_pointer(defs, pointer)
            except ValueError as e:
                raise ValueError(f"$ref points at unknown $defs entry: {ref} ({e})") from e
            # Recurse — the target might itself contain $refs
            return _inline_refs(target, defs)
        return {k: _inline_refs(v, defs) for k, v in node.items()}
    if isinstance(node, list):
        return [_inline_refs(item, defs) for item in node]
    return node


def _publishable_manifest_bytes(manifest_path: Path, defs: dict) -> bytes:
    """Read a manifest from disk and emit the bytes to publish — with all
    `$ref`s into the meta-schema's `$defs` inlined so the published artifact
    is self-contained at runtime."""
    raw = json.loads(manifest_path.read_text())
    inlined = _inline_refs(raw, defs)
    # Strip the $schema editor pointer — it references a relative path that
    # doesn't exist in S3, and runtime consumers ignore it anyway.
    inlined.pop("$schema", None)
    return json.dumps(inlined, indent=2, sort_keys=True).encode() + b"\n"


def _build_index(env: str, dirs: list[Path], meta: dict) -> dict:
    """Build index.json — every experiment in `dirs` becomes an entry.

    The hash field covers the *published* manifest bytes (post-$ref-inlining),
    not the source bytes. This way the hash matches what S3 actually serves.
    """
    defs = meta.get("$defs", {})
    entries = []
    for d in dirs:
        manifest_path = d / "manifest.json"
        instruction_path = d / "instruction.md"
        manifest_bytes = _publishable_manifest_bytes(manifest_path, defs)
        instruction_bytes = instruction_path.read_bytes()
        manifest = json.loads(manifest_bytes)
        entries.append({
            "id": manifest["id"],
            "version": manifest["version"],
            "manifest_key": f"{manifest['id']}/manifest.json",
            "instruction_key": f"{manifest['id']}/instruction.md",
            "hash": _hash_pair(manifest_bytes, instruction_bytes),
        })
    return {
        "published_at": datetime.now(timezone.utc).isoformat(),
        "git_sha": _git_sha(),
        "experiments": entries,
    }


def _git_sha() -> str:
    import subprocess
    try:
        out = subprocess.run(
            ["git", "rev-parse", "--short", "HEAD"],
            cwd=REPO_ROOT, capture_output=True, text=True, check=True,
        )
        return out.stdout.strip()
    except (subprocess.CalledProcessError, FileNotFoundError):
        return "unknown"


def _upload(s3, bucket: str, key: str, body: bytes, content_type: str) -> None:
    try:
        s3.put_object(Bucket=bucket, Key=key, Body=body, ContentType=content_type)
    except ClientError as e:
        print(f"  ✗ S3 PutObject failed for s3://{bucket}/{key}: {e}", file=sys.stderr)
        raise


def publish(env: str, only: list[str] | None = None, dry_run: bool = False) -> int:
    if env not in VALID_ENVS:
        print(f"error: --env must be one of {sorted(VALID_ENVS)}", file=sys.stderr)
        return 1

    bucket = f"agent-experiment-metadata-{env}"
    dirs = _experiment_dirs()
    if only:
        dirs = [d for d in dirs if d.name in only]
        if not dirs:
            print(f"error: no experiments matched --only={only}", file=sys.stderr)
            return 1

    print(f"== validating {len(dirs)} experiment(s) against meta-schema ==")
    meta = _load_meta_schema()
    _validate_all(meta, dirs)

    index = _build_index(env, dirs, meta)
    defs = meta.get("$defs", {})

    print(f"\n== publish target: s3://{bucket}/  (env={env}, git={index['git_sha']}) ==")
    print(f"   {len(index['experiments'])} experiment(s) to publish")

    if dry_run:
        print("\n[DRY RUN] would upload (manifest.json + instruction.md per experiment):")
        for entry in index["experiments"]:
            print(f"   {entry['id']} v{entry['version']} ({entry['hash'][:23]}...)")
        print("\n[DRY RUN] would write index.json LAST as atomic switch")
        return 0

    s3 = boto3.client("s3")

    # Upload per-experiment files in parallel, then index.json LAST so readers
    # never see a partial publish. The executor join (`as_completed` loop)
    # surfaces any per-file failures BEFORE we commit the atomic switch.
    with ThreadPoolExecutor(max_workers=10) as ex:
        futures = []
        for entry in index["experiments"]:
            exp_dir = EXPERIMENTS_DIR / entry["id"]
            # Inline $refs so the published manifest is self-contained — the
            # Lambda's Draft7Validator has no resolver for the meta-schema.
            manifest_bytes = _publishable_manifest_bytes(exp_dir / "manifest.json", defs)
            instruction_bytes = (exp_dir / "instruction.md").read_bytes()
            futures.append(ex.submit(
                _upload, s3, bucket, entry["manifest_key"],
                manifest_bytes, "application/json",
            ))
            futures.append(ex.submit(
                _upload, s3, bucket, entry["instruction_key"],
                instruction_bytes, "text/markdown",
            ))
        for f in as_completed(futures):
            f.result()  # surface failures before writing index.json

    for entry in index["experiments"]:
        print(f"   ✓ {entry['id']} v{entry['version']}")

    print("\n== writing index.json (atomic switch) ==")
    _upload(s3, bucket, "index.json",
            (json.dumps(index, indent=2) + "\n").encode(),
            "application/json")
    print(f"   ✓ s3://{bucket}/index.json ({len(index['experiments'])} experiments live)")
    return 0


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--env", required=True, help="Target env: dev | qa | prod")
    parser.add_argument("--only", action="append", default=None,
                        help="Only publish these experiment IDs (can be repeated)")
    parser.add_argument("--dry-run", action="store_true",
                        help="Validate + show what would publish, do not touch S3")
    args = parser.parse_args()
    return publish(env=args.env, only=args.only, dry_run=args.dry_run)


if __name__ == "__main__":
    raise SystemExit(main())
